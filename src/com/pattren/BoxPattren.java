package com.pattren;

public class BoxPattren {

	public static void main(String[] args) {
		
		for (int i=1; i<=4; i++) {
			//It is for Row's
			
			for (int j =1;j<=4;j++) {
				//It is for Columns
				
				System.out.print("*");
				//It is to print the *
				
			}
			System.out.println("");
			//It is going back to the i
		}

	}

}

